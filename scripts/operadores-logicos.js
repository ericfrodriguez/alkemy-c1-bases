
//* && (AND)

true  && true  == true; // Unico caso en el que es verdadero
false && true  == false;
true  && false == false;
false && false == false;

//* || (OR)

true  || true  == true;
false || true  == true;
true  || false == true;
false || false == false; // El unico caso en el que es false.

//* ! (NOT)

!true  == false;
!false == true;

